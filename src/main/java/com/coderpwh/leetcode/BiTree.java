package com.coderpwh.leetcode;

public class BiTree {


    private TreeNode root;


    public BiTree() {
        this.root = null;
    }


    public BiTree(TreeNode root) {
        this.root = root;
    }


    public TreeNode getRoot() {
        return root;
    }

    public void setRoot(TreeNode root) {
        this.root = root;
    }
}
